# üîπ –ó–∞–¥–∞–Ω–∏–µ: –ó–∞–ø–∏—Å–∞—Ç—å –∏ —Å—á–∏—Ç–∞—Ç—å –¥–∞–Ω–Ω—ã–µ –≤ JSON-—Ñ–æ—Ä–º–∞—Ç–µ.

import json

# –î–∞–Ω–Ω—ã–µ —Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º–∏
users = [
    {"name": "–ê–ª–µ–∫—Å", "age": 25},
    {"name": "–ú–∞—Ä–∏—è", "age": 30},
    {"name": "–ò–≤–∞–Ω", "age": 22}
]

# –ü—ã—Ç–∞–µ–º—Å—è —Å—á–∏—Ç–∞—Ç—å —Ñ–∞–π–ª, –µ—Å–ª–∏ –æ–Ω –µ—Å—Ç—å
try:
    with open("data.json", "r", encoding="utf-8") as file:
        data = json.load(file)  # –ó–∞–≥—Ä—É–∂–∞–µ–º JSON
    print("–î–∞–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞:", data)
except FileNotFoundError:
    print("–§–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω! –°–æ–∑–¥–∞—é –Ω–æ–≤—ã–π...")
    with open("data.json", "w", encoding="utf-8") as file:
        json.dump(users, file, indent=4, ensure_ascii=False)  # –ó–∞–ø–∏—Å—ã–≤–∞–µ–º `users`
    data = users  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∑–∞–ø–∏—Å–∞–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ

# –í—ã–≤–æ–¥ –≤–æ–∑—Ä–∞—Å—Ç–∞ –∫–∞–∂–¥–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
print("–í–æ–∑—Ä–∞—Å—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π:")
for user in data:
    print(f"{user['name']}: {user['age']} –ª–µ—Ç")